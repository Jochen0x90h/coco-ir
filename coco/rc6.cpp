// lower/upper limit for 440μs
constexpr int L444 = 7;
constexpr int T444 = 9;
constexpr int U444 = 10;

// lower/upper limit for 889μs
constexpr int L889 = 14;
constexpr int T889 = 18;
constexpr int U889 = 20;


bool decode(Array<const uint8_t> data, Packet &packet) {
    if (data.size() < 20)
        return false;

    // leader (2666μs 889μs)
    if (data[1] < 48 || data[1] > 59)
        return false;
    if (data[2] < L889 || data[2] > U889)
        return false;

    // start bit and mode bits (444μs 444μs)
    int index = 3;
    int t = 0;
    int bit = 1;
    int value = 0;
    for (int i = 0; i < 4; ++i) {
        if (t < L444)
            t = data[index++];
        else
            bit ^= 1;

        if (t < L444 || t > U444)
            return false;
        t = data[index++];
        t -= T444;

        value <<= 1;
        value |= bit;
    }
    packet.mode = value & 7;

    // trailer bit (889μs 889μs)
    {
        if (t < L889)
            t = data[index++];
        else
            bit ^= 1;

        if (t < L889 || t > U889)
            return false;
        t = data[index++];
        t -= T889;

        packet.trailer = bit;
    }

    // control and data bits (444μs 444μs)
    value = 0;
    for (int i = 0; i < 16; ++i) {
        if (t < L444) {
            if (index >= data.size())
                return false;
            t = data[index++];
        } else {
            bit ^= 1;
        }

        if (t < L444 || t > U444)
            return false;
        if (index >= data.size())
            t = 0;
        else
            t = data[index++];
        t -= T444;

        value <<= 1;
        value |= bit;
    }
    packet.control = value >> 8;
    packet.data = value;

    return true;
}

